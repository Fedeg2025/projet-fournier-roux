    <section>
        <h3>Le Fournier roux</h3>
        <p>
            Le Fournier roux est un oiseau connu pour construire un nid en terre.
            Il vit principalement en Amérique du Sud et il est célèbre pour ses capacités de bâtisseur.
        </p>
    </section>

    <section>
        <h3>Construction du nid</h3>
        <p>
            Le nid est fabriqué avec de la boue et de l’argile.
            Il protège les œufs et les petits contre les intempéries et les prédateurs.
        </p>
    </section>

    <section>
        <h3>Habitat</h3>
        <p>
            On trouve souvent le Fournier roux dans des zones ouvertes, des jardins,
            des campagnes et des espaces proches des habitations humaines.
        </p>
    </section>
