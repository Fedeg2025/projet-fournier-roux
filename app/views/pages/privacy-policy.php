     PAGE POLITIQUE DE CONFIDENTIALITÉ
     Cette page informe les utilisateurs
     sur la gestion de leurs données personnelles
     ========================= -->

<main id="main-content" class="page-legale page-legale--confidentialite">

    <!-- =========================
         TITRE PRINCIPAL
         ========================= -->
    <h1 class="page-legale__title">Politique de confidentialité</h1>

    <!-- =========================
         ARTICLE 1 - OBJET
         ========================= -->
    <section class="page-legale__section">
        <h2 class="page-legale__subtitle">1. Objet</h2>
        <p class="page-legale__text">
            La présente politique de confidentialité a pour objectif d’informer les utilisateurs du site
            sur la manière dont leurs données personnelles sont collectées, utilisées, stockées et protégées
            dans le cadre de l’utilisation du site.
        </p>
    </section>

    <!-- =========================
         ARTICLE 2 - DONNÉES COLLECTÉES
         ========================= -->
    <section class="page-legale__section">
        <h2 class="page-legale__subtitle">2. Données collectées</h2>
        <p class="page-legale__text">
            Dans le cadre du fonctionnement du site, certaines données personnelles peuvent être collectées,
            notamment :
        </p>
        <ul class="page-legale__list">
            <li class="page-legale__list-item">le nom ;</li>
            <li class="page-legale__list-item">le prénom ;</li>
            <li class="page-legale__list-item">l’adresse e-mail ;</li>
            <li class="page-legale__list-item">les informations liées au compte utilisateur ;</li>
            <li class="page-legale__list-item">les données saisies dans le formulaire de contact ;</li>
            <li class="page-legale__list-item">les informations nécessaires à l’authentification et à la gestion de session.</li>
        </ul>
    </section>

    <!-- =========================
         ARTICLE 3 - FINALITÉS
         ========================= -->
    <section class="page-legale__section">
        <h2 class="page-legale__subtitle">3. Finalités du traitement</h2>
        <p class="page-legale__text">
            Les données personnelles collectées sont utilisées uniquement dans le cadre des finalités suivantes :
        </p>
        <ul class="page-legale__list">
            <li class="page-legale__list-item">création et gestion des comptes utilisateurs ;</li>
            <li class="page-legale__list-item">connexion et sécurisation de l’accès à l’espace utilisateur ;</li>
            <li class="page-legale__list-item">gestion des demandes envoyées via le formulaire de contact ;</li>
            <li class="page-legale__list-item">administration du site et du back-office ;</li>
            <li class="page-legale__list-item">suppression ou anonymisation du compte utilisateur lorsque cela est demandé ;</li>
            <li class="page-legale__list-item">sécurisation technique et bon fonctionnement général du site.</li>
        </ul>
    </section>

    <!-- =========================
         ARTICLE 4 - BASE LÉGALE
         ========================= -->
    <section class="page-legale__section">
        <h2 class="page-legale__subtitle">4. Base légale</h2>
        <p class="page-legale__text">
            Le traitement des données personnelles repose sur l’exécution du service proposé par le site,
            sur la gestion de la relation avec les utilisateurs, ainsi que sur l’intérêt légitime lié à la
            sécurité, à la maintenance et à l’administration du projet.
        </p>
    </section>

    <!-- =========================
         ARTICLE 5 - DESTINATAIRES
         ========================= -->
    <section class="page-legale__section">
        <h2 class="page-legale__subtitle">5. Destinataires des données</h2>
        <p class="page-legale__text">
            Les données personnelles sont destinées uniquement aux personnes habilitées à administrer le site
            dans le cadre du projet. Elles ne sont ni vendues, ni cédées, ni utilisées à des fins commerciales.
        </p>
    </section>

    <!-- =========================
         ARTICLE 6 - DURÉE DE CONSERVATION
         ========================= -->
    <section class="page-legale__section">
        <h2 class="page-legale__subtitle">6. Durée de conservation</h2>
        <p class="page-legale__text">
            Les données personnelles sont conservées pendant une durée strictement nécessaire au fonctionnement
            du site, à la gestion des comptes utilisateurs, au traitement des demandes, ainsi qu’au respect des
            exigences techniques, administratives et de sécurité du projet.
        </p>
        <p class="page-legale__text">
            Lorsqu’un compte est supprimé, certaines données peuvent être supprimées ou anonymisées selon les
            besoins techniques du site, la cohérence de la base de données et les exigences minimales de sécurité
            et de traçabilité.
        </p>
    </section>

    <!-- =========================
         ARTICLE 7 - SUPPRESSION / ANONYMISATION
         ========================= -->
    <section class="page-legale__section">
        <h2 class="page-legale__subtitle">7. Suppression de compte et anonymisation</h2>
        <p class="page-legale__text">
            L’utilisateur peut demander la suppression de son compte directement depuis son espace personnel.
        </p>
        <p class="page-legale__text">
            Afin de préserver l’intégrité de la base de données et des contenus associés, la suppression
            du compte peut être mise en œuvre sous la forme d’une <strong>anonymisation</strong>.
        </p>
        <p class="page-legale__text">
            Cette anonymisation consiste à remplacer les informations directement identifiantes du compte
            utilisateur par des données anonymisées, tout en conservant les éléments strictement nécessaires
            au fonctionnement technique du site.
        </p>
        <p class="page-legale__text">
            Certaines informations minimales peuvent être conservées lorsqu’elles sont strictement nécessaires
            à la sécurité, à la cohérence technique du site ou à la traçabilité administrative.
        </p>
    </section>

    <!-- =========================
         ARTICLE 8 - SÉCURITÉ
         ========================= -->
    <section class="page-legale__section">
        <h2 class="page-legale__subtitle">8. Sécurité</h2>
        <p class="page-legale__text">
            Des mesures techniques ont été mises en place afin de protéger les données personnelles contre
            l’accès non autorisé, la perte, l’altération ou la divulgation.
        </p>
        <p class="page-legale__text">
            Le projet intègre notamment des mécanismes de sécurité tels que la protection contre les injections SQL,
            la sécurisation des formulaires, la gestion des sessions, la validation des données saisies,
            ainsi que l’utilisation de mots de passe chiffrés.
        </p>
    </section>

    <!-- =========================
         ARTICLE 9 - DROITS UTILISATEURS
         ========================= -->
    <section class="page-legale__section">
        <h2 class="page-legale__subtitle">9. Droits des utilisateurs</h2>
        <p class="page-legale__text">
            Conformément à la réglementation applicable, chaque utilisateur dispose notamment des droits suivants :
        </p>
        <ul class="page-legale__list">
            <li class="page-legale__list-item">droit d’accès à ses données ;</li>
            <li class="page-legale__list-item">droit de rectification ;</li>
            <li class="page-legale__list-item">droit à l’effacement ;</li>
            <li class="page-legale__list-item">droit à la limitation du traitement, dans certains cas ;</li>
            <li class="page-legale__list-item">droit d’opposition, lorsque cela est applicable.</li>
        </ul>
        <p class="page-legale__text">
            Toute demande relative à l’exercice de ces droits peut être formulée via les moyens de contact
            mis à disposition sur le site.
        </p>
    </section>

    <!-- =========================
         ARTICLE 10 - COOKIES
         ========================= -->
    <section class="page-legale__section">
        <h2 class="page-legale__subtitle">10. Cookies et sessions</h2>
        <p class="page-legale__text">
            Le site peut utiliser des cookies techniques ou des mécanismes équivalents strictement nécessaires
            au fonctionnement des sessions utilisateur, à l’authentification et à la navigation sécurisée.
        </p>
    </section>

    <!-- =========================
         ARTICLE 11 - MODIFICATION
         ========================= -->
    <section class="page-legale__section">
        <h2 class="page-legale__subtitle">11. Modification de la politique</h2>
        <p class="page-legale__text">
            La présente politique de confidentialité peut être mise à jour à tout moment afin de refléter
            les évolutions du projet, de ses fonctionnalités ou des obligations applicables.
        </p>
    </section>

</main>