    <h2>Politique de confidentialité</h2>

    <section>
        <h3>1. Objet</h3>
        <p>
            La présente politique de confidentialité a pour objectif d’informer les utilisateurs du site
            sur la manière dont leurs données personnelles sont collectées, utilisées, stockées et protégées
            dans le cadre de l’utilisation du site.
        </p>
    </section>

    <section>
        <h3>2. Données collectées</h3>
        <p>
            Dans le cadre du fonctionnement du site, certaines données personnelles peuvent être collectées,
            notamment :
        </p>
        <ul>
            <li>le nom ;</li>
            <li>le prénom ;</li>
            <li>l’adresse e-mail ;</li>
            <li>les informations liées au compte utilisateur ;</li>
            <li>les données saisies dans le formulaire de contact ;</li>
            <li>les informations nécessaires à l’authentification et à la gestion de session.</li>
        </ul>
    </section>

    <section>
        <h3>3. Finalités du traitement</h3>
        <p>
            Les données personnelles collectées sont utilisées uniquement dans le cadre des finalités suivantes :
        </p>
        <ul>
            <li>création et gestion des comptes utilisateurs ;</li>
            <li>connexion et sécurisation de l’accès à l’espace utilisateur ;</li>
            <li>gestion des demandes envoyées via le formulaire de contact ;</li>
            <li>administration du site et du back-office ;</li>
            <li>traitement des demandes de suppression de compte ;</li>
            <li>sécurisation technique et bon fonctionnement général du site.</li>
        </ul>
    </section>

    <section>
        <h3>4. Base légale</h3>
        <p>
            Le traitement des données personnelles repose sur l’exécution du service proposé par le site,
            sur la gestion de la relation avec les utilisateurs, ainsi que sur l’intérêt légitime lié à la
            sécurité, à la maintenance et à l’administration du projet.
        </p>
    </section>

    <section>
        <h3>5. Destinataires des données</h3>
        <p>
            Les données personnelles sont destinées uniquement aux personnes habilitées à administrer le site
            dans le cadre du projet. Elles ne sont ni vendues, ni cédées, ni utilisées à des fins commerciales.
        </p>
    </section>

    <section>
        <h3>6. Durée de conservation</h3>
        <p>
            Les données personnelles sont conservées pendant une durée strictement nécessaire au fonctionnement
            du site, à la gestion des comptes utilisateurs, au traitement des demandes, ainsi qu’au respect des
            exigences techniques, administratives et de sécurité du projet.
        </p>
        <p>
            Certaines données peuvent être conservées de manière limitée lorsqu’elles sont nécessaires à la
            cohérence technique de la base de données, à la prévention d’abus, à la sécurité du site ou à la
            traçabilité administrative.
        </p>
    </section>

    <section>
        <h3>7. Suppression de compte et anonymisation</h3>
        <p>
            L’utilisateur peut demander la suppression de son compte depuis son espace personnel.
            Cette demande n’est pas exécutée automatiquement : elle est transmise à un administrateur
            et traitée dans le back-office.
        </p>
        <p>
            Afin de préserver l’intégrité de la base de données et des contenus associés, la suppression
            du compte peut être mise en œuvre sous la forme d’une <strong>anonymisation</strong>.
        </p>
        <p>
            Cette anonymisation consiste à remplacer les informations directement identifiantes du compte
            utilisateur par des données anonymisées, tout en conservant les éléments strictement nécessaires
            au fonctionnement technique du site.
        </p>
        <p>
            Lorsqu’une demande de suppression est effectuée, certaines informations liées à cette demande
            peuvent être conservées sous forme de trace administrative minimale au moment de la demande,
            afin d’assurer le suivi, la sécurité et la traçabilité du traitement.
        </p>
    </section>

    <section>
        <h3>8. Sécurité</h3>
        <p>
            Des mesures techniques ont été mises en place afin de protéger les données personnelles contre
            l’accès non autorisé, la perte, l’altération ou la divulgation.
        </p>
        <p>
            Le projet intègre notamment des mécanismes de sécurité tels que la protection contre les injections SQL,
            la sécurisation des formulaires, la gestion des sessions, la validation des données saisies,
            ainsi que l’utilisation de mots de passe chiffrés.
        </p>
    </section>

    <section>
        <h3>9. Droits des utilisateurs</h3>
        <p>
            Conformément à la réglementation applicable, chaque utilisateur dispose notamment des droits suivants :
        </p>
        <ul>
            <li>droit d’accès à ses données ;</li>
            <li>droit de rectification ;</li>
            <li>droit à l’effacement ;</li>
            <li>droit à la limitation du traitement, dans certains cas ;</li>
            <li>droit d’opposition, lorsque cela est applicable.</li>
        </ul>
        <p>
            Toute demande relative à l’exercice de ces droits peut être formulée via les moyens de contact
            mis à disposition sur le site.
        </p>
    </section>

    <section>
        <h3>10. Cookies et sessions</h3>
        <p>
            Le site peut utiliser des cookies techniques ou des mécanismes équivalents strictement nécessaires
            au fonctionnement des sessions utilisateur, à l’authentification et à la navigation sécurisée.
        </p>
    </section>

    <section>
        <h3>11. Modification de la politique</h3>
        <p>
            La présente politique de confidentialité peut être mise à jour à tout moment afin de refléter
            les évolutions du projet, de ses fonctionnalités ou des obligations applicables.
        </p>
    </section>
