                     ========================= -->
                <section class="oiseau-article__section oiseau-article__section--description oiseau-article__section--with-image" aria-labelledby="description-fournier">
                    <div class="oiseau-article__section-content">
                        <h2 id="description-fournier" class="oiseau-article__subtitle">Description générale</h2>

                        <p class="oiseau-article__text">
                            Le Fournier roux est l’une des espèces les plus représentatives de la famille des Furnariidés.
                            C’est un oiseau de taille moyenne, d’aspect simple mais facile à reconnaître grâce à sa coloration
                            brun-roux, à sa silhouette trapue et à sa queue courte. Il est très visible dans les paysages
                            ouverts et se montre souvent peu farouche à proximité des habitations.
                        </p>

                        <p class="oiseau-article__text">
                            Son nom commun fait directement référence à son nid caractéristique en forme de four traditionnel,
                            construit en boue durcie. Cette architecture remarquable a largement contribué à sa renommée dans
                            toute l’Amérique du Sud.
                        </p>
                    </div>

