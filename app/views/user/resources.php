                <!-- =========================
                     LE SAVIEZ-VOUS ?
                     ========================= -->
                <aside class="oiseau-article__section oiseau-article__section--info" aria-labelledby="info-fournier">
                    <h2 id="info-fournier" class="oiseau-article__subtitle">Le saviez-vous ?</h2>

                    <p class="oiseau-article__text">
                        Pendant la période de nidification, les glandes salivaires du Fournier roux deviennent plus actives.
                        Cette particularité facilite le mélange de la boue et améliore la cohésion des matériaux utilisés
                        pour construire le nid.
                    </p>

                    <p class="oiseau-article__text">
                        Une fois abandonné, le nid peut être réutilisé par d’autres oiseaux ou de petits animaux comme abri
                        ou comme site de reproduction.
                    </p>
                </aside>

                <!-- =========================
                     STATUT DE CONSERVATION
                     ========================= -->
                <section class="oiseau-article__section oiseau-article__section--conservation" aria-labelledby="conservation-fournier">
                    <h2 id="conservation-fournier" class="oiseau-article__subtitle">Statut de conservation</h2>

                    <p class="oiseau-article__text">
                        Le Fournier roux est classé en catégorie <strong>Préoccupation mineure (LC)</strong>. L’espèce est
                        commune dans l’ensemble de son aire de répartition et semble bien s’adapter aux milieux transformés
                        par l’être humain. Sa population est considérée comme stable, voire localement en augmentation.
                    </p>
                </section>

                <!-- =========================
                     IMPORTANCE CULTURELLE
                     ========================= -->
                <section class="oiseau-article__section oiseau-article__section--culture" aria-labelledby="culture-fournier">
                    <h2 id="culture-fournier" class="oiseau-article__subtitle">Importance culturelle</h2>

                    <p class="oiseau-article__text">
                        En Argentine, le Fournier roux possède une forte valeur symbolique et est reconnu comme oiseau national.
                        Il est souvent associé au travail, à la persévérance et à la fidélité du couple, en raison de son
                        comportement et de l’ingéniosité de son nid.
                    </p>
                </section>

