                <!-- =========================
                     LE NID
                     ========================= -->
                <section class="oiseau-article__section oiseau-article__section--nid oiseau-article__section--with-image" aria-labelledby="nid-fournier">
                    <div class="oiseau-article__section-content">
                        <h2 id="nid-fournier" class="oiseau-article__subtitle">Le nid</h2>

                        <p class="oiseau-article__text">
                            Le nid du Fournier roux est l’un des éléments les plus remarquables de l’espèce. Il est construit à partir
                            de boue, de salive, de paille, de racines et d’autres fibres végétales. Une fois séché, il devient très
                            solide et peut atteindre plusieurs kilogrammes.
                        </p>

                        <p class="oiseau-article__text">
                            Sa forme rappelle celle d’un petit four en terre, avec une entrée qui conduit à un vestibule puis à une
                            chambre interne de nidification. Cette structure protège efficacement les œufs et les oisillons contre
                            les intempéries et certains prédateurs.
                        </p>

                        <p class="oiseau-article__text">
                            Le nid est généralement installé en hauteur, sur des branches horizontales, des poteaux, des clôtures,
                            des toits ou d’autres supports artificiels. Le couple construit le plus souvent un nouveau nid à chaque
                            saison plutôt que de réutiliser celui de l’année précédente.
                        </p>
                    </div>

