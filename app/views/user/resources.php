                <h1 id="oiseau-title" class="oiseau-article__title">Fournier roux</h1>

                <p class="oiseau-article__latin">
                    <em>Furnarius rufus</em>
                </p>

                <p class="oiseau-article__intro">
                    Le Fournier roux est un passereau emblématique d’Amérique du Sud,
                    facilement reconnaissable à son plumage brun-roux, à son chant sonore et surtout à son célèbre
                    nid de boue en forme de four. Espèce familière des milieux ouverts, ruraux et urbains, il est
                    particulièrement apprécié pour son comportement territorial, sa fidélité au couple et son
                    étonnante capacité de construction.
                </p>
