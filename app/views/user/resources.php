                <!-- =========================
                     IDENTIFICATION
                     ========================= -->
                <section class="oiseau-article__section oiseau-article__section--identification" aria-labelledby="identification-fournier">
                    <h2 id="identification-fournier" class="oiseau-article__subtitle">Identification</h2>

                    <p class="oiseau-article__text">
                        Le plumage du Fournier roux est majoritairement brun roux à cannelle sur le dos, les ailes et la queue.
                        Les rémiges primaires sont plus sombres, tandis que les secondaires présentent des nuances plus roussâtres.
                        Les parties inférieures, comme la poitrine, les flancs et le ventre, sont plus claires, dans des tons
                        beige à fauve pâle. La gorge apparaît souvent plus blanchâtre.
                    </p>

                    <p class="oiseau-article__text">
                        La tête reste dans la continuité des couleurs du corps, avec parfois une calotte légèrement plus brune
                        et un front ou une nuque plus roux. Le bec est presque droit, de longueur moyenne, gris brunâtre,
                        les pattes sont grises et l’iris brun-roux. La queue courte et rousse constitue l’un de ses traits
                        les plus distinctifs.
                    </p>

                    <p class="oiseau-article__text">
                        Il n’existe pas de dimorphisme sexuel marqué : le mâle et la femelle se ressemblent beaucoup.
                        Les juvéniles sont en général plus pâles sur les parties inférieures.
                    </p>
                </section>

                <!-- =========================
                     VOIX ET COMPORTEMENT
                     ========================= -->
                <section class="oiseau-article__section oiseau-article__section--comportement" aria-labelledby="comportement-fournier">
                    <h2 id="comportement-fournier" class="oiseau-article__subtitle">Voix et comportement</h2>

                    <p class="oiseau-article__text">
                        Le Fournier roux est un oiseau très vocal et peu discret. Son chant est fort, rauque et facilement
                        reconnaissable. Il chante souvent en couple, dans une sorte de duo coordonné : le mâle émet
                        généralement des notes rapides et perçantes, auxquelles la femelle répond par des sons plus aigus
                        et plus espacés.
                    </p>

                    <p class="oiseau-article__text">
                        Espèce sédentaire, territoriale et monogame, il se laisse observer facilement. On le rencontre
                        seul ou en couple, le plus souvent au sol, où il marche avec assurance en recherchant sa nourriture.
                        Sa familiarité avec les espaces habités explique sa présence fréquente dans les jardins, les parcs,
                        les chemins et les zones rurales.
                    </p>
                </section>

                <!-- =========================
                     HABITAT
                     ========================= -->
                <section class="oiseau-article__section oiseau-article__section--habitat" aria-labelledby="habitat-fournier">
                    <h2 id="habitat-fournier" class="oiseau-article__subtitle">Habitat</h2>

                    <p class="oiseau-article__text">
                        Le Fournier roux vit surtout dans les milieux ouverts et semi-ouverts, secs ou humides. Il fréquente
                        les savanes, prairies, broussailles, lisières, pâtures, zones agricoles, parcs urbains, jardins
                        et terrains vagues. Il peut également occuper des bois clairs ou des espaces modifiés par l’activité humaine.
                    </p>

                    <p class="oiseau-article__text">
                        Très adaptable, il prospère particulièrement bien dans les environnements anthropisés, ce qui a favorisé
                        sa présence durable dans de nombreuses régions.
                    </p>
                </section>

                <!-- =========================
                     RÉPARTITION
                     ========================= -->
                <section class="oiseau-article__section oiseau-article__section--repartition" aria-labelledby="repartition-fournier">
                    <h2 id="repartition-fournier" class="oiseau-article__subtitle">Répartition</h2>

                    <p class="oiseau-article__text">
                        Le Fournier roux est largement réparti dans le centre et le sud de l’Amérique du Sud.
                        On le trouve en Argentine, en Uruguay, au Paraguay, en Bolivie et au Brésil. En Argentine,
                        il possède une valeur symbolique particulière puisqu’il est considéré comme l’oiseau national.
                    </p>

                    <p class="oiseau-article__text">
                        Son adaptation aux paysages transformés par l’être humain contribue à la stabilité de sa répartition
                        et parfois même à son expansion locale.
                    </p>
                </section>

                <!-- =========================
                     ALIMENTATION
                     ========================= -->
                <section class="oiseau-article__section oiseau-article__section--alimentation" aria-labelledby="alimentation-fournier">
                    <h2 id="alimentation-fournier" class="oiseau-article__subtitle">Alimentation</h2>

                    <p class="oiseau-article__text">
                        Son régime alimentaire repose principalement sur les invertébrés. Il consomme surtout des insectes,
                        notamment des coléoptères, des fourmis, des termites, des criquets, des sauterelles et des larves.
                        Il mange aussi des vers, des araignées, des escargots et d’autres petits invertébrés.
                    </p>

                    <p class="oiseau-article__text">
                        Il cherche sa nourriture essentiellement au sol, sur des terrains dégagés, dans la litière de feuilles
                        ou dans l’herbe rase. Il peut aussi compléter son alimentation avec quelques graines.
                    </p>
                </section>

                <!-- =========================
                     REPRODUCTION
                     ========================= -->
                <section class="oiseau-article__section oiseau-article__section--reproduction oiseau-article__section--with-image" aria-labelledby="reproduction-fournier">
                    <div class="oiseau-article__section-content">
                        <h2 id="reproduction-fournier" class="oiseau-article__subtitle">Reproduction</h2>

                        <p class="oiseau-article__text">
                            La reproduction a lieu principalement pendant le printemps et l’été austral. Le couple est généralement
                            stable et les deux partenaires participent à la construction du nid, à l’incubation et à l’élevage des jeunes.
                        </p>

                        <p class="oiseau-article__text">
                            La femelle pond en général de 2 à 5 œufs. L’incubation dure environ 14 à 18 jours et est assurée par
                            les deux parents. Les jeunes restent au nid entre 23 et 26 jours avant l’envol, puis peuvent demeurer
                            plusieurs mois sur le territoire des adultes.
                        </p>
                    </div>

